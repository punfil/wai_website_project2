    <div id="seria_800" class="naglowek">
        Seria 800
    </div>
    <div class="seria_laptop">
        Seria 800 została opracowana jako mobilna linia maszyn PowerPC Reference Platform (PReP), która miała być bezpośrednim następcą starszego notebooka RISC System / 6000 Model 7007-N40. Chociaż przypominały maszyny RISC System / 6000, w rzeczywistości nie są maszynami RISC System / 6000 i są systemami Power Personal. Miało to tę zaletę, że umożliwiało im uruchamianie innych systemów operacyjnych opartych na PowerPC przy pomocy Dakota (nazwa kodowa Power Personal System Firmware), a nie tylko AIX.
        Ponieważ każdy model z serii 800 był maszynami zgodnymi z PReP, były one w stanie obsługiwać systemy operacyjne oparte wyłącznie na PowerPC i związane z nimi aplikacje. Systemy operacyjne to: Microsoft Windows NT 3.51 i 4.0, IBM OS / 2 Warp (PowerPC Edition), IBM AIX 4.1.x i 4.2.x, Sun Solaris 2.5.1 oraz Linux (jądra oparte na PowerPC). Niestety, nie byli w stanie uruchomić Apple System 7 lub Mac OS 8, ponieważ nie były zgodne z Common Hardware Reference Platform (CHRP).
        Ta seria nie trwała jednak długo, ponieważ ilość programów, które zostały stworzone dla architektury PowerPC była bardzo mała. Co więcej, zakres cen większości modeli był bardzo skandalicznie drogi, sięgając nawet 18 000 USD (bez uwzględnienia inflacji). W wyniku słabej sprzedaży seria 800 została przerwana i nigdy nie miała zostać przywrócona z następcą.
        Z tej serii nie posiadam laptopów.
        <img class="center" src="static/img/ThinkPad_850.png" alt="IBM ThinkPad 850" title="IBM ThinkPad 850" />
    </div>
