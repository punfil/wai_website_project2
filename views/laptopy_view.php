    <div class="seria_laptop">
        Seria R została opracowana jako bezpośredni następca zaprzestanej serii 300. Oprócz braku modeli konwertowalnych 2 w 1, służył temu samemu celowi, co seria 300.
        Począwszy od R50, seria R przekształciła się w zasadniczo duplikat serii T, która była grubsza i miała mniej funkcji. Miał również mniej luksusową konstrukcję, ponieważ jego wieczko nie było gumowane, jak w przypadku serii T. Kolejną istotną różnicą było to, że posiadał FireWire, czego nie posiadała seria T aż do wprowadzenia T61.
        Ostatecznie seria R została przerwana i została pośrednio zastąpiona serią SL. Jednak Lenovo odkupiło go z powrotem, aby służyć jako bardziej luksusowy element boczny dla serii E. Nowa seria R ma teraz identyczną konstrukcję jak jej odpowiedniki z serii E i ma aluminiowe pokrywy, które identyfikują ją jako luksusowy odpowiednik serii E.
